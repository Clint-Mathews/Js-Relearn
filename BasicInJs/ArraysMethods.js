
let fruits = ["Apple", "Orange", "Grapes"];

console.log(fruits);

// Push

fruits.push("Watermelon");
console.log(fruits);

// Pop
let fruit = fruits.pop();
console.log(fruit);
console.log(fruits);

// Unshift - add element in front of the array

fruits.unshift(fruit);
console.log(fruits);

// Shift - remove element from front of the array

fruit = fruits.shift();
console.log(fruit);
console.log(fruits);